package P9.Praktikum2;

/**
 * Nasabah
 */
public class Nasabah {

    String noRek, nama, alamat;
    int umur;
    double saldo;

    public Nasabah(String noRek, String nama, String alamat, int umur, double saldo) {
        this.noRek = noRek;
        this.nama = nama;
        this.alamat = alamat;
        this.umur = umur;
        this.saldo = saldo;
    }
}